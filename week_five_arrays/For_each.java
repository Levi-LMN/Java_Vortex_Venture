/*
for each loop is used to traverse array or collection in java. It is easier to use than for loop and it is more readable.
 */
public class For_each {
    public static void main(String[] args) {
        String[] food = {"chapati", "rice", "Ugali", "beans"};
        for (String i : food){
            System.out.println(i);
        }

    }
}