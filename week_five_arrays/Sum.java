/*
a method that receves4 integer values and returns the sum of the four values
 */
package week_five_arrays;

public class Sum {
    public static void main(String[] args) {
        System.out.println(sum(2, 3, 4, 5));
    }

    public static int sum(int x, int y, int z, int a) {
        return x + y + z + a;
    }
}